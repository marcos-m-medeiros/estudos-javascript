// Inicialize a variável 'txt' com o valor 1
let txt = 1;

// Inicia um loop while que continuará enquanto 'txt' for menor ou igual a 11
while (txt <= 11) {
  // Imprime a mensagem "Hello World!" no console a cada iteração do loop
  console.log("Hello World! " + txt);

  // Incrementa a variável 'txt' em 1 a cada iteração
  txt++;
}

// O loop continuará até que 'txt' seja maior que 11, momento em que ele será encerrado
