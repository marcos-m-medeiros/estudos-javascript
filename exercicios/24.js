// Inicialize a variável 'txt' com o valor 0
let txt = 0;

// Inicia um loop while que continuará enquanto 'txt' for menor ou igual a 10
while (txt <= 10) {
  // Imprime a mensagem "Hello World!" no console a cada iteração do loop
  console.log("Hello World!");

  // Incrementa a variável 'txt' em 1 a cada iteração
  txt++;
}

// O loop continuará até que 'txt' seja maior que 10, momento em que ele será encerrado
