// Um loop "for" que começa com i=1, executa enquanto i for menor ou igual a 10 e incrementa i em cada iteração.
for (let i = 1; i <= 10; i++) {
    console.log(`contador = ${i}`); // Imprime o valor atual de i.
}

// Um array chamado "notas" com valores numéricos.
const notas = [6.7, 7.4, 9.8, 8.1, 7.7];

// Um loop "for" que percorre o array "notas" usando a variável "i" como índice.
// Ele começa com i=0, executa enquanto i for menor do que o tamanho do array "notas", e incrementa i em cada iteração.
for (let i = 0; i < notas.length; i++) {
    console.log(`Nota = ${notas[i]}`); // Imprime o valor da nota no índice atual de "i".
}
